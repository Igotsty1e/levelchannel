3.3. Специальные категории персональных данных (статья 10 № 152-ФЗ) и биометрические персональные данные (статья 11 № 152-ФЗ) в рамках настоящего Поручения **не обрабатываются**. Учитель обязан воздерживаться от внесения таких сведений в Учётные записи Учеников.

## 4. Цели обработки

4.1. Учитель обрабатывает персональные данные Учеников исключительно в следующих целях:

— ведение списка учеников Учителя в рамках функционала Сервиса;
— составление, изменение и поддержание актуального расписания уроков;
— учёт баланса пакетов уроков и истории проведённых занятий;
— направление через функционал Сервиса информационных сообщений, относящихся к учебному процессу (например, изменение времени урока, истечение пакета).

4.2. Обработка персональных данных Учеников в маркетинговых целях, для рассылки рекламных сообщений, для составления портретов или иных профилей вне учебного процесса Учителем по настоящему Поручению **не допускается**.

