— расписание уроков и проведённых занятий (даты, время, длительность);
— баланс пакетов уроков и история их использования (количество приобретённых, проведённых, отменённых уроков);
— иные сведения, которые Учитель самостоятельно вносит в Учётную запись Ученика через интерфейс Сервиса в пределах, разрешённых функционалом.

